package com.em.core.sys.dictionary.type.entity;

public class SysDictionaryType {
    private String dicTypeId;

    private String typeName;

    private String typeCode;

    private String isUse;
    
    private String notId;

	/**
	 * @return the dicTypeId
	 */
	public String getDicTypeId() {
		return dicTypeId;
	}

	/**
	 * @param dicTypeId the dicTypeId to set
	 */
	public void setDicTypeId(String dicTypeId) {
		this.dicTypeId = dicTypeId;
	}

	/**
	 * @return the typeName
	 */
	public String getTypeName() {
		return typeName;
	}

	/**
	 * @param typeName the typeName to set
	 */
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	/**
	 * @return the typeCode
	 */
	public String getTypeCode() {
		return typeCode;
	}

	/**
	 * @param typeCode the typeCode to set
	 */
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}

	/**
	 * @return the isUse
	 */
	public String getIsUse() {
		return isUse;
	}

	/**
	 * @param isUse the isUse to set
	 */
	public void setIsUse(String isUse) {
		this.isUse = isUse;
	}

	/**
	 * @return the notId
	 */
	public String getNotId() {
		return notId;
	}

	/**
	 * @param notId the notId to set
	 */
	public void setNotId(String notId) {
		this.notId = notId;
	}


    
}